def home_or_away(name):
    if game_dict()["home"]["team_name"]==name:
        return "home"
    if game_dict()["away"]["team_name"]==name:
        return "away"

def team_colors(name):
    team=home_or_away(name)
    if game_dict()[team]["team_name"]==name:
        return game_dict()[team]["colors"]


print(team_colors("Cleveland Cavaliers"))

def player_numbers(name):
     team=home_or_away(name)
     if game_dict()[team]["team_name"]==name:
        return [x["number"] for x in game_dict()[team]["players"] ]
   
     
print(player_numbers("Cleveland Cavaliers"))

