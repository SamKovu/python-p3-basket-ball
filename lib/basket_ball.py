def shoe_brands():
     brands=[]
     for player in get_all_players():
        if player["shoe_brand"] not in brands:
            brands.append(player["shoe_brand"])
     return brands

print(shoe_brands())

def shoe_brand_dic():
    brands={}
    for brand in shoe_brands():
        brands.update({brand:[]})
    
    for brand in shoe_brands():
        for player in get_all_players():
            if player["shoe_brand"]==brand:
                brands[brand].append(player["rebounds_per_game"])

    return brands

print (shoe_brand_dic())

print("\n\n")

def mean(numbers):
    total=0
    for n in numbers:
        total=total+n
    return total/len(numbers)

def average_rebounds_by_shoe_brand():
    for key,value in shoe_brand_dic().items():
        x = '{0:.2f}'.format(mean(value))
        print(f"{key}:  {x}")

